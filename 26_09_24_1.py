''' here ie the example of logical operators '''

print("Using 'AND' Operater")
sub1 = 56
sub2 = 78
sub3 = 6
res = (sub1>=35 and sub2>=35 and sub3>=35)
print(res)


print("Using 'OR' Operater")
sub1 = 56
sub2 = 78
sub3 = 6
res = (sub1>=35 or sub2>=35 or sub3>=35)
print(res)


print("Using 'NOT' Operater")
sub1 = 56
sub2 = 78
sub3 = 6
res = (not(sub1>=35 and sub2>=35 and sub3>=35))
print(res)