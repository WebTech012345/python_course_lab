''' here is the below example for bitwise operators '''
a=5
b=6
print("a :",a)
print("b :",b)
print("a&b :",a&b)
print("a|b :",a|b)
print("a^b :",a^b)
print("~a :",~a)
print("a<<b :",a<<b)
print("a>>b :",a>>b)

a=5
b=6
print("binary of a :",bin(a))
print("binary of b :",bin(b))
print("binary of a&b :",bin(a&b))
print("binary of a|b :",bin(a|b))
print("binary of a^b :",bin(a^b))
print("binary of ~a :",bin(~a))
print("binary of a<<b :",bin(a<<b))
print("binary of a>>b :",bin(a>>b))
