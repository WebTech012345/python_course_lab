''' here is the example of comparison operators '''
a=32
b=6
print("two numbers are equal or not",a==b)
print("two numbers are not equal or not",a!=b)
print("a is less than or equal to b or not",a<=b)
print("a is greater than or equal to b or not",a>=b)
print("a is less than b or not",a<b)
print("a is greater than b or not",a>b)
